"""API request and response data models used by FastAPI endpoints."""
from __future__ import annotations

from pydantic import BaseModel, Field


class QueryRequest(BaseModel):
    """Request body containing the user's search query."""

    query: str = Field(..., description="User-supplied natural language query")


class SentimentResponse(BaseModel):
    """Sentiment analysis output."""

    label: str = Field(..., description="Predicted sentiment label, e.g. POSITIVE or NEGATIVE")
    score: float = Field(..., ge=0.0, le=1.0, description="Confidence score (0.0 â€“ 1.0)")


class SummaryResponse(BaseModel):
    """Summary text generated by the LLM."""

    summary: str = Field(..., description="Concise summary answering the query based on context documents")
